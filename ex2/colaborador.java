package ex2;

public class colaborador {
    public int codigo;
    public String nome;
    public int telefone;
    public double salario_base;

    public colaborador(int codigo, String nome, int telefone, double salario_base) {
        this.codigo = codigo;
        this.nome = nome;
        this.telefone = telefone;
        this.salario_base = salario_base;
    }
}
